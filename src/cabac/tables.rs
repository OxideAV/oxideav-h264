//! CABAC context initialization tables and per-slice context builder.
//!
//! Transcribed from ITU-T H.264 (07/2019) Tables 9-12..9-33, covering
//! every ctxIdx in `0..460`.
//!
//! ## Layout
//!
//! Each ctxIdx `i` maps to four `(m, n)` initialization pairs:
//!
//! * `INIT_MN[i][0]` — Table 9-12: the I / SI slice initialization
//!   table (used when `slice_type == I || slice_type == SI`). The
//!   I/SI table only defines values for a subset of ctxIdx ranges
//!   (skip flag, sub-mb types, MVD, ref-idx are not used for I).
//!   Unused entries are `(0, 0)` — see below.
//! * `INIT_MN[i][1]` — cabac_init_idc = 0, from the P/B table set.
//! * `INIT_MN[i][2]` — cabac_init_idc = 1.
//! * `INIT_MN[i][3]` — cabac_init_idc = 2.
//!
//! Values were transcribed directly from ITU-T H.264 (07/2019), not
//! copied from any source code.  Spots marked `NA` in the spec
//! (typically for context ranges that are only legal in one slice
//! family) are stored as `(0, 0)` — the initialization formula still
//! produces a valid probability state for such a pair, but decoders
//! must never reference those slots from the wrong slice type.
//!
//! ## ctxIdx ranges (Table 9-34)
//!
//! | Range | Syntax element                        |
//! |-------|---------------------------------------|
//! | 0-2   | mb_type (SI slices)                   |
//! | 3-10  | mb_type (I slices) + shared I/P/B     |
//! | 11-13 | mb_skip_flag (P/SP)                   |
//! | 14-20 | mb_type (P/SP)                        |
//! | 21-23 | sub_mb_type (P/SP)                    |
//! | 24-26 | mb_skip_flag (B)                      |
//! | 27-35 | mb_type (B)                           |
//! | 36-39 | sub_mb_type (B)                       |
//! | 40-53 | mvd_l0 / mvd_l1 (x component)         |
//! | 47-53 | mvd (y component, same table split)   |
//! | 54-59 | ref_idx_l0 / ref_idx_l1               |
//! | 60-63 | mb_qp_delta                           |
//! | 64-67 | intra_chroma_pred_mode                |
//! | 68    | prev_intra4x4_pred_mode_flag          |
//! | 69    | rem_intra4x4_pred_mode                |
//! | 70-72 | mb_field_decoding_flag                |
//! | 73-76 | coded_block_pattern (luma)            |
//! | 77-84 | coded_block_pattern (chroma)          |
//! | 85-104| coded_block_flag                      |
//! |105-165| significant_coeff_flag (frame coded)  |
//! |166-226| last_significant_coeff_flag (frame)   |
//! |227-275| coeff_abs_level_minus1                |
//! |276    | end_of_slice_flag                     |
//! |277-337| significant_coeff_flag (field coded)  |
//! |338-398| last_significant_coeff_flag (field)   |
//! |399-459| coeff_abs_level_minus1 (field)        |
//!
//! NB: the full 1024-context span documented in the 2019 revision
//! covers transform_size_8x8_flag and the 8×8/4×4 coefficient coding
//! contexts for High profile.  For v1 (Baseline/Main parity) we stop
//! at `NUM_CTX = 460`, matching the original H.264 spec.


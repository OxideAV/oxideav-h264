/// The ctxIdx of the `end_of_slice_flag` context, which is fixed in
/// the spec as `pStateIdx=63, valMPS=0` regardless of SliceQPY.
const END_OF_SLICE_CTX_IDX: usize = 276;

/// Build a fresh 460-entry context vector for a slice.
///
/// * `cabac_init_idc` — the syntax element from the slice header;
///   only used when `slice_type_is_intra == false` (range 0..=2).
/// * `slice_type_is_intra` — true for I or SI slices (selects
///   Table 9-12 column); false selects one of the three P/B tables.
/// * `slice_qpy` — `SliceQPY` per §7.4.3: used in the §9.3.1.1
///   formula.
pub fn init_slice_contexts(
    cabac_init_idc: u32,
    slice_type_is_intra: bool,
    slice_qpy: i32,
) -> Vec<CabacContext> {
    let col = if slice_type_is_intra {
        0
    } else {
        // cabac_init_idc is 0..=2 per the spec; clamp defensively.
        let idc = cabac_init_idc.min(2) as usize;
        idc + 1
    };

    let mut ctxs = Vec::with_capacity(NUM_CTX);
    for (i, row) in INIT_MN_DATA.iter().enumerate() {
        if i == END_OF_SLICE_CTX_IDX {
            // Fixed probability: pStateIdx=63, valMPS=0.
            ctxs.push(CabacContext {
                p_state_idx: 63,
                val_mps: 0,
            });
            continue;
        }
        let (m, n) = row[col];
        ctxs.push(CabacContext::init(m, n, slice_qpy));
    }
    ctxs
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn table_has_expected_rows() {
        assert_eq!(INIT_MN.len(), NUM_CTX);
        // Full 1024-slot layout from ITU-T H.264 (2019); base 0..=459
        // covers Main profile, 460..=1023 covers High Profile 8×8 +
        // the 4:4:4 Cb/Cr extension banks (§9.3.3.1.1.9 Table 9-42).
        assert_eq!(NUM_CTX, 1024);
    }

    #[test]
    fn luma8x8_cbf_init_mn_at_spec_slot() {
        // Sanity check: ctxIdx 1012..=1015 init pairs for I slices
        // should match the transcription checked against FFmpeg's
        // `cabac_context_init_I` at the same indices.
        let v = [(-3, 70), (-8, 93), (-10, 90), (-30, 127)];
        for (k, expected) in v.iter().enumerate() {
            assert_eq!(INIT_MN[1012 + k][0], *expected);
        }
    }

    #[test]
    fn cb_16x16dc_sig_init_matches_luma() {
        // Cb 16x16 DC sig bank at ctxIdx 484 is initialised with the
        // exact same (m,n) row set as luma cat=0 at ctxIdx 105 — the
        // "separation" in 4:4:4 is runtime state divergence, not init.
        for k in 0..15 {
            assert_eq!(INIT_MN[484 + k], INIT_MN[105 + k]);
        }
    }

    #[test]
    fn end_of_slice_is_fixed() {
        let ctxs = init_slice_contexts(0, true, 26);
        let eos = &ctxs[CTX_IDX_END_OF_SLICE];
        assert_eq!(eos.p_state_idx, 63);
        assert_eq!(eos.val_mps, 0);
    }

    #[test]
    fn intra_column_is_index_zero() {
        // For I slices, we pull column 0. Sanity-check with the
        // first row (mb_type[0], Table 9-12): m=20, n=-15.
        // At SliceQPY=26:
        //   pre = Clip3(1,126, (20*26)>>4 + -15) = Clip3(1,126, 32 + -15) = 17
        //   17 <= 63 -> pStateIdx = 46, valMPS = 0
        let ctxs = init_slice_contexts(0, true, 26);
        assert_eq!(ctxs[0].p_state_idx, 46);
        assert_eq!(ctxs[0].val_mps, 0);
    }
}

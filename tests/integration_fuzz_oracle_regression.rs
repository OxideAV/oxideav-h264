/// actually contains. The relevant fields:
///
/// * 4:2:0 (inferred — chroma_format_idc isn't carried for
///   profile_idc=66), `frame_mbs_only_flag = 1` →
///   `CropUnitX = 2`, `CropUnitY = 2`.
/// * `pic_width_in_mbs_minus1 = 177` → `PicWidthInSamplesL = 2848`.
/// * `pic_height_in_map_units_minus1 = 0` → `FrameHeightInSamplesL = 16`.
/// * `frame_crop_top_offset = 2148` → `CropUnitY * (top + bottom + 1)
